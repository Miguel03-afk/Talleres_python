# Ingresar cualquier cantidad de horas // es al revez ahora del 5
horas = float(input("Ingrese la cantidad de horas: "))

# multiplicar por 60 siempre pues los minutos en 1 hora seran siempre daran los mismo 60 solo queda poner cualquier hora por 60 y el resultado daran los minutos e
minutos = horas * 60
# Mostrar el resultado
print(f"{horas} hora(s) equivalen a {minutos} minutos.")
